package com.example.azakary.Model;

public class AzkarModel {

    String elzekr, counter;

    public AzkarModel(String elzekr, String counter) {
        this.elzekr = elzekr;
        this.counter = counter;
    }

    public String getElzekr() {
        return elzekr;
    }

    public void setElzekr(String elzekr) {
        this.elzekr = elzekr;
    }

    public String getCounter() {
        return counter;
    }

    public void setCounter(String counter) {
        this.counter = counter;
    }
}
